/** 챕터 개수 */
function chapterCount(type) {
  return (OUTLINES[type] || []).length;
}

/** 목차만 (제목 리스트) */
function titles(type) {
  return (OUTLINES[type] || []).map((c) => c.title);
}

/* ============================================================
 * 리포트 성격 구분
 *
 *   종합사주   → "이 사람이 어떤 사람인가"
 *   전문 리포트 → "그 분야에서 어떻게 행동하고 어떤 흐름을 겪는가"
 *
 * ai.js 가 이 구분을 보고 프롬프트를 다르게 준다.
 * pdfDoc.js 가 이 구분을 보고 용어 풀이 페이지를 뺀다.
 * ============================================================ */
const SPECIALIST = ['신년운세', '연애운', '결혼운', '재물운', '건강운', '연인궁합', '재회운'];

function isSpecialist(type) {
  return SPECIALIST.indexOf(type) >= 0;
}

module.exports = { OUTLINES, chapterCount, titles, SPECIALIST, isSpecialist };

/* ============================================================
 * 질문 답변 챕터
 *
 * 내담자가 신청할 때 "묻고 싶은 것"을 적으면, 그 질문만 따로
 * 깊게 답하는 챕터를 만든다.
 *
 * ⚠️ 위치는 앞쪽(2번째). 예전에는 맨 뒤였는데,
 *    거기까지 읽기 전에 지쳐서 "질문 답이 없다"는 피드백이 나왔다.
 * ============================================================ */

const QUESTION_CHAPTER = {
  title: '남겨주신 질문에 답합니다',
  sub: [
    '먼저, 물어보신 것에 대한 답',
    '왜 그렇게 보는지',
    '흐름이 바뀌는 시기',
    '지금 할 수 있는 것 / 하지 말아야 할 것',
  ],
};

/**
 * 질문이 있으면 질문 답변 챕터를 앞쪽에 끼워 넣는다.
 * @param {string} type 리포트 종류
 * @param {string|null} question 내담자 질문
 */
function outlineWithQuestion(type, question) {
  const base = OUTLINES[type] || OUTLINES['종합사주'];
  const q = (question || '').trim();
  if (!q) return base;

  const out = base.slice();
  // 마지막 '종합 정리' 장 바로 앞에 넣는다.
  //   앞쪽에 두면 본문을 읽기도 전에 답이 나와 흐름이 끊긴다.
  //   전체 풀이를 다 본 뒤, 마무리 직전에 답하는 것이 자연스럽다.
  const at = Math.max(1, out.length - 1);
  out.splice(at, 0, QUESTION_CHAPTER);
  return out;
}

module.exports.QUESTION_CHAPTER = QUESTION_CHAPTER;
module.exports.outlineWithQuestion = outlineWithQuestion;
