/** AI 프롬프트에 넣을 블록 생성 */
function fieldBlock(type) {
  const d = DOMAIN[type];
  if (!d) return '';

  const parts = [
    '[이 리포트에서 반드시 근거로 삼을 명식 항목]',
    d.primary.map((x) => `- ${x}`).join('\n'),
  ];

  if (d.timing && d.timing.length) {
    parts.push('\n[시기 판정 근거]');
    parts.push(d.timing.map((x) => `- ${x}`).join('\n'));
  }

  parts.push('\n[상품 경계 — 반드시 지킬 것]');
  parts.push(d.rule);

  return parts.join('\n');
}

module.exports = { DOMAIN, fieldBlock };
